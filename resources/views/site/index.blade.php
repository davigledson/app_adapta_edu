        <div class="content visao-ciencia">
            <h1>Como o TOD é descrito pelo DSM-V?</h1>
            <p>O manual diagnóstico e estatístico de transtornos mentais é um documento que foi criado pela Associação Americana de Psiquiatria (APA) para padronizar os critérios diagnósticos das desordens que afetam a mente e as emoções. Segundo ele, o TOD é um padrão de humor raivoso/irritável, de comportamento questionador/desafiante ou índole vingativa com duração de pelo menos seis meses, como evidenciado por pelo menos quatro sintomas de qualquer das categorias seguintes e exibido na interação com pelo menos um indivíduo
                que não seja um irmão.</p>
            <p>Especificar a gravidade atual:</p>
            <ol>
               <p><strong>Leve:</strong> Os sintomas limitam-se a apenas um ambiente (p. ex., em casa, na escola, no trabalho,
                com os colegas).</p> 
                <p><strong>Moderado:</strong> Alguns sintomas estão presentes em pelo menos dois ambientes.</p> 
                <p><strong>Grave:</strong> Alguns sintomas estão presentes em três ou mais ambientes.</p> 
            </ol><br><br>
            
            <h2><strong>Características Associadas que Apoiam o Diagnóstico</strong></h2>
            <p>Em crianças e adolescentes, o transtorno de oposição desafiante é mais prevalente em famílias
                nas quais o cuidado da criança é perturbado por uma sucessão de cuidadores diferentes ou em
                famílias nas quais são comuns práticas agressivas, inconsistentes ou negligentes de criação dos
                filhos. Duas das condições que mais costumam ocorrer de forma concomitante com o transtorno
                de oposição desafiante são o transtorno de déficit de atenção/hiperatividade (TDAH) e o transtorno da conduta (ver a seção “Comorbidade” para esse transtorno). O transtorno de oposição
                desafiante foi associado a um risco aumentado para tentativas de suicídio, mesmo depois do
                controle para transtornos comórbidos.</p><br><br>
            
            <h2><strong>Prevalência</strong></h2>
            <p>A prevalência do transtorno de oposição desafiante varia de 1 a 11%, com uma prevalência média
                estimada de 3,3%. A taxa da transtorno pode variar de acordo com a idade e o gênero da criança.
                Aparentemente, é mais prevalente em indivíduos do sexo masculino do que em indivíduos do
                sexo feminino (1,4:1) antes da adolescência. Essa predominância do sexo masculino não é encontrada de forma consistente em amostras de adolescentes ou de adultos.</p><br><br>
            
            <h2><strong>Fatores de Risco e Prognóstico</strong></h2>
            <ol>
                <p><strong>temperamentais:</strong> Fatores temperamentais relacionados a problemas de regulação emocional
                (p. ex., níveis elevados de reatividade emocional, baixa tolerância a frustrações) são preditivos
                do transtorno.</p>
                <p><strong>Ambientais:</strong> Práticas agressivas, inconsistentes ou negligentes de criação dos filhos são comuns
                em famílias de crianças e adolescentes com transtorno de oposição desafiante, sendo que essas
                práticas parentais desempenham papel importante em muitas teorias causais do transtorno.</p>
                <p><strong>Genéticos e fisiológicos:</strong> Uma série de marcadores neurobiológicos (p. ex., menor reatividade
                da frequência cardíaca e da condutância da pele; reatividade do cortisol basal reduzida; anormalidades no córtex pré-frontal e na amígdala) foi associada ao transtorno de oposição desafiante.
                Entretanto, a vasta maioria dos estudos não separou crianças com o transtorno de oposição desafiante daquelas com transtorno da conduta. Desse modo, não está claro se existem marcadores
                específicos para o transtorno de oposição desafiante.
                </p>
            </ol><br><br>
            <p><strong>Referências</strong></p>
            <p>APA – AMERICAN PSYCHIATRIC ASSOCIATION.<b> Manual diagnóstico e estatístico de transtornos mentais:</b> DSM-5. Tradução de Maria Inês Corrêa Nascimento. 5º. ed. Porto Alegre: Artmed, 2014, p. 462-466.</p>
