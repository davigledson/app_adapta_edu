    <h3><img src="./img/seta-direita.png"><a href="http://pepsic.bvsalud.org/scielo.php?script=sci_arttext&pid=S1808-56872011000200005">Avaliação e intervenção psicoterapêutica nos transtornos disruptivos: algumas reflexões</a></h3>
       <p><strong>RESUMO</strong></p>
        <p>Os transtornos disruptivos são os transtornos psiquiátricos mais frequentes na infância e têm grande impacto na adolescência e na vida adulta. Porém, há uma série de empecilhos para seu diagnóstico: a) dificuldade com o diagnóstico diferencial, principalmente em relação a ansiedade, depressão e déficit de atenção/hiperatividade; b) dificuldade em apontar quando o comportamento faz parte do ciclo normal do desenvolvimento infantil; e c) escassez de instrumentos e técnicas padronizadas para esse tipo de atendimento. Portanto, este artigo teórico tem por objetivo refletir sobre os aspectos considerados necessários e importantes para o atendimento psicoterápico nos transtornos disruptivos, sem pretensão de esgotar a discussão. Para tanto, inicialmente serão apresentadas a definição e a caracterização de transtorno da conduta e transtorno desafiador de oposição. A partir desse ponto serão levantadas questões sobre avaliação, passando por entrevista clínica, análise funcional e inventários, e sobre as técnicas de intervenção, discutindo-se aspectos relevantes do treinamento de pais e do treinamento de crianças e adolescentes.</p>
    <br><br>
    <h3><img src="./img/seta-direita.png"><a href="https://www.scielo.br/j/sausoc/a/cDLQxzdXt53HpxszVgKfkLr/?lang=pt">Dispositivos de segurança, psiquiatria e prevenção da criminalidade: o TOD e a noção de criança perigosa</a></h3>
       <p><strong>RESUMO</strong></p>
        <p>O artigo analisa a tendência crescente da multiplicação de diagnósticos psiquiátricos na infância, que encontra legitimidade no argumento que indica que se as patologias psiquiátricas da infância não forem devidamente tratadas, será altamente provável que, na vida adulta, surjam graves problemas psiquiátricos irreversíveis, assim como problemas associados à criminalidade e à delinquência. Analisa-se o exemplo do transtorno de oposição e desafio, conhecido como TOD, na medida em que, de acordo com o DSM-5, essa patologia supõe um alto risco para o desenvolvimento do transtorno de personalidade antissocial, patologia que apresenta conotações claramente jurídicas e criminológicas. Esses diagnósticos são analisados em perspectiva crítica a partir do conceito foucaultiano de “dispositivo de segurança”</p>
    <br><br>
    <h3><img src="./img/seta-direita.png"><a href="https://www.scielo.br/j/rbp/a/7S44bNFFLpKBzTzVzXkSJDG/?lang=pt&format=pdf">Transtorno desafiador de oposição: uma revisão de
        correlatos neurobiológicos e ambientais, comorbidades, tratamento e prognóstico</a></h3>
        </p>
        <p><strong>RESUMO</strong></p>
            <p>Transtorno desafiador de oposição (TDO) é uma entidade diagnóstica independente, mas é freqüentemente estudada em conjunto com transtorno de déficit de atenção/hiperatividade (TDAH) ou com transtorno de conduta (TC). O objetivo deste artigo é o de
            fazer uma revisão das evidências existentes, obtidas por meio da base de dados PubMed, sobre achados neurobiológicos no
            transtorno desafiador de oposição, funcionamento familiar e escolar, comorbidades, prognóstico e opções terapêuticas para
            transtorno desafiador de oposição. A evidência de correlatos hormonais, genéticos e neurofuncionais de transtorno desafiador de
            oposição, a conexão com a família, as relações e desempenho escolares, a associação com transtornos do humor, ansiosos e
            disruptivos, o risco de evolução para transtorno de conduta e de persistência de sintomas de transtorno desafiador de oposição
            são descritos. Uma revisão do efeito da Terapia Cognitivo-Comportamental e tratamento farmacológico é apresentada. A análise
            das evidências disponíveis mostra que o impacto de transtorno desafiador de oposição não deve ser ignorado e que o transtorno
            desafiador de oposição deve ser devidamente abordado. O impacto do tratamento de transtorno desafiador de oposição no
            prognóstico de longo prazo dos pacientes ainda precisa ser determinado.</p>
    <br><br>
    <h3><img src="./img/seta-direita.png"><a href="https://fatece.edu.br/arquivos/arquivos-revistas/trilhas/volume10_2/Suelen%20Fernandes%20da%20Silva;%20Camila%20S.%20C.%20A.%20de%20M.%20Herculian.pdf">TRANSTORNO OPOSITOR DESAFIADOR (TOD) NO AMBIENTE ESCOLAR</a></h3>
    <p><strong>RESUMO</strong></p>
        <p>O Transtorno Opositor Desafiador (TOD) é um transtorno neuropsíquico de
        comportamento destrutível, com apresentação de comportamentos que fazem mal a
        própria criança e aos outros com ela convivem. Em sua grande maioria, as crianças com
        TOD, apresentam um padrão negativista, desobediente e de desafio com pessoas de
        autoridade, como professores e responsáveis, resultando em inúmeros aspectos negativos
        no que se refere ao desenvolvimento e aprendizagem das crianças. Através de revisão
        bibliográfica esse estudo traz o enfrentamento do TOD no ambiente escolar e tem como
        objetivo geral a necessidade de se compreender de que forma o TOD pode ser enfrentado
        pelos responsáveis dentro do ambiente escolar, o que, consequentemente, contribui de
        forma direta para a melhora na qualidade da aprendizagem dos alunos. Também mostra
        através de seus objetivos específicos os distúrbios de conduta humana; aspectos gerais do
        TOD; presença do TOD na infância e adolescência; descreve como ocorre a presença de
        TOD na infância e adolescência e faz uma reflexão sobre como o TOD pode prejudicar a
        aprendizagem dos alunos. É de suma importância que a identificação do TOD seja
        realizada o quanto antes e que o tratamento se inicie logo após o diagnóstico, para que
        escola e família já sejam orientados e saibam como lidar com a criança e para que essa
        tenha um desenvolvimento, no ambiente escolar e espaços sociais, de maneira ampla e
        assertiva, construindo todos os instrumentos necessários para a sua formação como
        sujeito social.</p>
